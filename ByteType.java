
public class ByteType {
	public static void main(String[] args) {
		byte a=5;
		Byte aval=5;
		System.out.println("BYTE: "+aval.MAX_VALUE);
		System.out.println("BYTE: "+aval.MIN_VALUE);
		
		
		short s=1254;
		Short sval=1254;
		System.out.println("SHORT: "+sval.MAX_VALUE);
		System.out.println("SHORT: "+sval.MIN_VALUE);
		
		int i=121_123_213;
		Integer ival=123_250;
		System.out.println("INT: " +ival.MIN_VALUE);
		System.out.println("INT: "+ival.MAX_VALUE);
		
		
		long l=250_215;
		Long lval=321_213L;
		System.out.println("LONG: "+Long.MAX_VALUE);
		System.out.println("LONG: "+Long.MIN_VALUE);
		
		
		
		
	}
	
}
